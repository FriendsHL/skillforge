    func testPersonalAppCardMatchesTargetHierarchyInLightMode() {
        continueAfterFailure = false
        let app = XCUIApplication()
        app.launchArguments = ["--ui-testing-interactive-artifact", "--personal-app-card-light"]
        app.launch()

        let card = app.descendants(matching: .any)["attachment.card.interactive-budget"]
        XCTAssertTrue(card.waitForExistence(timeout: 5))
        let summary = app.descendants(matching: .any)["personalApp.summary.interactive-budget"]
        XCTAssertTrue(summary.exists)
        XCTAssertTrue(summary.label.contains("July budget"))
        XCTAssertTrue(summary.label.contains("Personal App"))
        XCTAssertFalse(summary.label.contains("Generated by Agent"))
        XCTAssertFalse(summary.label.contains("Research Agent"))

        let capabilities = app.staticTexts["personalApp.capabilities.interactive-budget"]
        XCTAssertTrue(capabilities.exists)
        XCTAssertEqual(capabilities.label, "Offline · No permissions")
        let provenance = app.staticTexts["personalApp.provenance.interactive-budget"]
        XCTAssertTrue(provenance.exists)
        XCTAssertTrue(provenance.label.hasPrefix("Research Agent · "))
        XCTAssertTrue(provenance.label.contains("Jul 17, 2026"))
        XCTAssertTrue(provenance.label.contains("02:30") || provenance.label.contains("2:30"))
        XCTAssertLessThan(capabilities.frame.minX, provenance.frame.minX)
        XCTAssertGreaterThan(provenance.frame.maxX, capabilities.frame.maxX)

        let open = app.buttons["attachment.open.interactive-budget"]
        XCTAssertTrue(open.exists)
        XCTAssertEqual(open.label, "Open July budget")
        XCTAssertGreaterThanOrEqual(open.frame.height + 0.001, 44)

        let share = app.buttons["attachment.share.interactive-budget"]
        XCTAssertTrue(share.exists)
        XCTAssertEqual(share.label, "Share July budget")
        XCTAssertGreaterThanOrEqual(share.frame.width + 0.001, 44)
        XCTAssertGreaterThanOrEqual(share.frame.height + 0.001, 44)

        let screenshot = XCTAttachment(screenshot: app.screenshot())
        screenshot.name = "Personal App card · light"
        screenshot.lifetime = .keepAlways
        add(screenshot)
    }

