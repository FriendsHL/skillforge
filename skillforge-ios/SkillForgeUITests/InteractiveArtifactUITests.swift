    private func launchPersonalAppViewer(
        extraLaunchArguments: [String] = []
    ) -> XCUIApplication {
        let app = XCUIApplication()
        app.launchArguments = ["--ui-testing-interactive-artifact"] + extraLaunchArguments
        app.launch()

        let open = app.buttons["attachment.open.interactive-budget"]
        XCTAssertTrue(open.waitForExistence(timeout: 5))
        open.tap()
        XCTAssertTrue(app.descendants(matching: .any)["personalApp.viewer.interactive-budget"]
            .waitForExistence(timeout: 5))
        return app
    }

    private func debugValue(_ identifier: String, in app: XCUIApplication) -> String {
        let element = app.descendants(matching: .any)[identifier]
        XCTAssertTrue(element.waitForExistence(timeout: 5), identifier)
        return element.label
    }

    private func waitForPersonalAppConfirmationCooldown() {
        RunLoop.current.run(
            until: Date().addingTimeInterval(PersonalAppConfirmationCooldown.uiTestInterval)
        )
    }
}

private enum PersonalAppConfirmationCooldown {
    static let uiTestInterval: TimeInterval = 2.1
}
