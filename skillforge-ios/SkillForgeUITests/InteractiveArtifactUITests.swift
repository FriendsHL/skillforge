        waitForPersonalAppConfirmationCooldown()
        submit.tap()
        XCTAssertTrue(app.alerts["Send this state to Agent?"].waitForExistence(timeout: 5))
        app.alerts.buttons["Send"].tap()
        XCTAssertTrue(app.staticTexts.matching(NSPredicate(
            format: "label CONTAINS %@", "Personal App July budget state"
        )).firstMatch.waitForExistence(timeout: 5))
    }

    func testPersonalAppExternalURLRequiresConfirmationAndOpensExactlyOnce() {
        continueAfterFailure = false
        let app = launchPersonalAppViewer()
        let expectedURL = "https://example.com:8443/report?q=skillforge%20ios#summary"

        XCTAssertEqual(debugValue("personalApp.debug.openCount", in: app), "0")
        let request = app.webViews.buttons["Open external report"]
        XCTAssertTrue(request.waitForExistence(timeout: 5))
        request.tap()

        let alert = app.alerts["Open external link?"]
        XCTAssertTrue(alert.waitForExistence(timeout: 5))
        XCTAssertTrue(alert.staticTexts.matching(NSPredicate(
            format: "label CONTAINS %@", "example.com"
        )).firstMatch.exists)
        XCTAssertTrue(alert.staticTexts.matching(NSPredicate(
            format: "label CONTAINS %@", expectedURL
        )).firstMatch.exists)
        XCTAssertEqual(debugValue("personalApp.debug.openCount", in: app), "0")

        alert.buttons["Cancel"].tap()
        XCTAssertEqual(debugValue("personalApp.debug.openCount", in: app), "0")
        XCTAssertTrue(app.descendants(matching: .any)["personalApp.viewer.interactive-budget"].exists)

        waitForPersonalAppConfirmationCooldown()
        request.tap()
        XCTAssertTrue(alert.waitForExistence(timeout: 5))
        alert.buttons["Open"].tap()
        XCTAssertEqual(debugValue("personalApp.debug.openCount", in: app), "1")
        XCTAssertEqual(debugValue("personalApp.debug.lastOpenedURL", in: app), expectedURL)
        XCTAssertTrue(app.descendants(matching: .any)["personalApp.viewer.interactive-budget"].exists)
    }

