    func testPersonalAppClipboardRuntimeGuardPreservesNativePasteboard() {
        continueAfterFailure = false
        let sentinel = "SKILLFORGE_CLIPBOARD_BEFORE"
        UIPasteboard.general.string = sentinel
        XCTAssertEqual(UIPasteboard.general.string, sentinel)
        let app = launchPersonalAppViewer()

        let request = app.webViews.buttons["Try clipboard"]
        XCTAssertTrue(request.waitForExistence(timeout: 5))
        request.tap()
        let marker = app.webViews.staticTexts.matching(NSPredicate(
            format: "label CONTAINS %@", "Clipboard denied · href="
        )).firstMatch
        XCTAssertTrue(marker.waitForExistence(timeout: 5))
        XCTAssertTrue(marker.label.contains("href=about:blank"))
        XCTAssertTrue(marker.label.contains("origin=null"))
        XCTAssertTrue(marker.label.contains("secure=false"))
        XCTAssertTrue(marker.label.contains("api=undefined"))
        XCTAssertTrue(marker.label.contains("read=unavailable"))
        XCTAssertTrue(marker.label.contains("write=unavailable"))
        XCTAssertTrue(marker.label.contains("exec=false"))
        XCTAssertEqual(UIPasteboard.general.string, sentinel)
        let springboard = XCUIApplication(bundleIdentifier: "com.apple.springboard")
        XCTAssertFalse(springboard.alerts.firstMatch.exists)
        XCTAssertTrue(app.descendants(matching: .any)["personalApp.viewer.interactive-budget"].exists)
    }

