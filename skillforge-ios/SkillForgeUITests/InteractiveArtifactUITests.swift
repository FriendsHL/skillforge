    func testPersonalAppRequiresNativeConfirmationBeforeSendingSnapshot() {
        continueAfterFailure = false
        let app = XCUIApplication()
        app.launchArguments = ["--ui-testing-interactive-artifact"]
        app.launch()

        let card = app.descendants(matching: .any)["attachment.card.interactive-budget"]
        XCTAssertTrue(card.waitForExistence(timeout: 5))
        let summary = app.descendants(matching: .any)["personalApp.summary.interactive-budget"]
        XCTAssertTrue(summary.label.contains("Personal App"))
        XCTAssertEqual(
            app.staticTexts["personalApp.capabilities.interactive-budget"].label,
            "Offline · No permissions"
        )
        XCTAssertTrue(app.staticTexts["personalApp.provenance.interactive-budget"].label
            .hasPrefix("Research Agent · "))

        let open = app.buttons["attachment.open.interactive-budget"]
        XCTAssertTrue(open.waitForExistence(timeout: 5))
        open.tap()
        XCTAssertTrue(app.descendants(matching: .any)["personalApp.viewer.interactive-budget"]
            .waitForExistence(timeout: 5))
